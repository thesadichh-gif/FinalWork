#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <string.h>
#include <locale.h>

int main() {
    setlocale(LC_ALL, "RUS");
    char a1;
    printf("--- Часть 1: символ ---\n");
    printf("Введите символ: ");
    scanf(" %c", &a1);
    printf("Вы ввели символ: %c\n", a1);
    printf("Код символа (ASCII): %d\n\n", a1);

    while (getchar() != '\n');

    char a2[100];
    printf("--- Часть 2: строка ---\n");
    printf("Введите строку (минимум 2 слова, длина >= 15): ");
    fgets(a2, sizeof(a2), stdin);
    a2[strcspn(a2, "\n")] = '\0';
    size_t a3 = strlen(a2);
    printf("Вы ввели: \"%s\"\n", a2);
    printf("Длина строки: %zu символов\n\n", a3);

    int a4, a5;
    printf("--- Часть 3: целые числа (положительное и отрицательное) ---\n");
    printf("Введите положительное и отрицательное целые числа (например, 789 -76): ");
    scanf("%d %d", &a4, &a5);

    // Проверfка на положительность первой переменной a4
    if (a4 > 0) {
        printf("\n*** ПРОВЕРКА: Первое число %d — положительное ***\n", a4);
    }
    else if (a4 < 0) {
        printf("\n*** ПРОВЕРКА: Первое число %d — отрицательное (ошибка, ожидалось положительное) ***\n", a4);
    }
    else {
        printf("\n*** ПРОВЕРКА: Первое число равно нулю ***\n", a4);
    }

    printf("\nДесятичный формат (%%d):\n");
    printf("  Без коэффициента: %d, %d\n", a4, a5);
    printf("  С заданной шириной 10 (выравнивание вправо): %10d, %10d\n", a4, a5);
    printf("  С левой позиции (ширина 10, выравнивание влево): %-10d, %-10d\n", a4, a5);

    printf("\nШестнадцатеричный формат (%%x):\n");
    printf("  Без коэффициента: %x, %x\n", a4, a5);
    printf("  С заданной шириной 10 (выравнивание вправо): %10x, %10x\n", a4, a5);
    printf("  С левой позиции (ширина 10, выравнивание влево): %-10x, %-10x\n\n", a4, a5);

    float a6, a7;
    printf("--- Часть 4: числа с плавающей точкой (положительное и отрицательное) ---\n");
    printf("Введите положительное и отрицательное вещественные числа (например, 956.361 -395.659): ");
    scanf("%f %f", &a6, &a7);

    printf("\nДесятичная форма (%%f, точность 3 знака): %.3f, %.3f\n", a6, a7);
    printf("Экспоненциальная форма (%%e, точность 3 знака): %.3e, %.3e\n\n", a6, a7);

    unsigned int a8;
    printf("--- Часть 5: целое беззнаковое число ---\n");
    printf("Введите беззнаковое целое (например, 794): ");
    scanf("%u", &a8);

    printf("Десятичный беззнаковый (%%u): %u\n", a8);
    printf("Восьмеричный беззнаковый (%%o): %o\n", a8);
    printf("Шестнадцатеричный беззнаковый (%%x): %x\n", a8);
    
    getchar();
    getchar(); // чтобы консоль не закрывалась сразу
    return 0;
}